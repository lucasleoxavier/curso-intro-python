# -*- coding: utf-8 -*-

# Bloco If Else
var1 = 5
var2 = 3

if var1==var2:
    print('É igual')
else:
    print('Não é igual')

# Bloco If
if var1==var2:
    print('É igual')

if var1!=var2:
    print('Não é igual')

# Bloco Elif ( se usa ele ao invés de vários if/else encadeados)
if var1<var2:
    print('Var1 menor que Var2')
elif var1>var2:
    print('Var1 maior que Var2')
else:
    print('Var1 igual a Var2')